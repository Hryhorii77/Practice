package hw_15;

public class People {
    public String firstName;
    public String secondName;
    public int age;
    private int weight;
    public int height;
    public String hobby;
    public String colourHair;

    public People(String firstName, String secondName, int age, int weight, int height, String hobby, String colourHair) {
        this.firstName = firstName;
        this.secondName = secondName;
        this.age = age;
        this.weight = weight;
        this.height = height;
        this.hobby = hobby;
        this.colourHair = colourHair;
    }
}
