package lesson_12;

public class Car {
    public Car(String model, int wheels, int speed) {
        this.model = model;
        this.wheels = wheels;
        this.speed = speed;

    }

    public Car() {
    }

    public Car (String model) {
        this.model = model;
    }

    public String model;
    public int wheels;
    public int speed;
    public String colour;



}
